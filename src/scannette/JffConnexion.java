    private void jBtValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtValiderActionPerformed
        String identite = "";
        String mail = jfAdrMail.getText();
        String mdp = String.valueOf(jpfMDP.getPassword());

        String[] param = {mail, mdp};
        AccesAPI api = new AccesAPI("http://localhost/Formulaire", param);
        ArrayList<String> lesReponses = api.getReponse();

        String message;
        if (lesReponses.get(0).equals("null")) {
            message = "Adresse mail ou mot de passe erron√©";
        } else {
            for (String uneReponse : lesReponses) {
                System.out.println("Une reponse: " + uneReponse);
                identite += uneReponse + "";
            }
            message = "Bienvenue " + identite;
            Scannette laScannette = new Scannette(identite);
            JffScannette saisieArticles = new JffScannette(laScannette);
            saisieArticles.setVisible(true);
            dispose();
        }
        JOptionPane.showMessageDialog(null, message);

    }//GEN-LAST:event_jBtValiderActionPerformed

